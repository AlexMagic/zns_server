package com.zns.app.bean;

public class ExamInfo {
    private Integer recordid;

    private String examinationno;

    private String examinationtitle;

    private String examinfourl;

    public Integer getRecordid() {
        return recordid;
    }

    public void setRecordid(Integer recordid) {
        this.recordid = recordid;
    }

    public String getExaminationno() {
        return examinationno;
    }

    public void setExaminationno(String examinationno) {
        this.examinationno = examinationno;
    }

    public String getExaminationtitle() {
        return examinationtitle;
    }

    public void setExaminationtitle(String examinationtitle) {
        this.examinationtitle = examinationtitle;
    }

    public String getExaminfourl() {
        return examinfourl;
    }

    public void setExaminfourl(String examinfourl) {
        this.examinfourl = examinfourl;
    }
}